package com.example.crazydrive;

public class TopTenRecord {
    private double latitude;
    private double longitude;
    private long time;
    private long score;
    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public long getTime() {
        return time;
    }

    public long getScore() {
        return score;
    }

}
